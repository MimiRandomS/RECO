func_show_interfaces() {
    echo ">>> Interfaces de red con IP:"
    ip a
    echo ""
    read -p "Presiona Enter para continuar..."
}

func_show_routes() {
    echo ">>> Tabla de rutas:"
    netstat -r
    echo ""
    read -p "Presiona Enter para continuar..."
}

func_show_traffic() {
    echo ">>> Tr├Аfico de red por interfaz:"
    netstat -i
    echo ""
    read -p "Presiona Enter para continuar..."
}

func_show_ethtool() {
    read -p "Introduce el nombre de la interfaz (ej. eth0): " interfaz
    echo ">>> Detalles de la interfaz $interfaz:"
    ethtool "$interfaz"
    echo ""
    read -p "Presiona Enter para continuar..."
}

func_show_packages() {
    read -p "Introduce la interfaz para escuchar (ej. eth0): " interfaz
    echo ">>> Mostrando paquetes en tiempo real (presiona Ctrl+C para salir)..."
    tcpdump -i "$interfaz"
}

func_show_ports() {
    if ! command -v lsof >/dev/null 2>&1; then
        echo "Error: lsof no est├А instalado."
        exit 1
    fi

    echo "---------------------------------------------"
    echo "|  PUERTO  |   PROTOCOLO   |     SERVICIO    |"
    echo "---------------------------------------------"
    echo "|   22     |     TCP       |     SSH         |"
    echo "|   25     |     TCP       |     SMTP        |"
    echo "|   53     |     TCP/UDP   |     DNS         |"
    echo "|   80     |     TCP       |     HTTP        |"
    echo "|  110     |     TCP       |     POP3        |"
    echo "|  143     |     TCP       |     IMAP        |"
    echo "|  443     |     TCP       |     HTTPS       |"
    echo "|  631     |     TCP       |     IPP (CUPS)  |"
    echo "| 3306     |     TCP       |     MySQL       |"
    echo "| 5432     |     TCP       |     PostgreSQL  |"
    echo "| 6379     |     TCP       |     Redis       |"
    echo "---------------------------------------------"
    echo

    read -p "Introduce el n├║mero de puerto a verificar: " PORT
    echo

    RESULT=$(lsof -i :$PORT 2>/dev/null)

    if [ -n "$RESULT" ]; then
        echo "Puerto $PORT est├А ABIERTO."
        echo "Servicio(s) usando el puerto:"
        echo "$RESULT" | awk 'NR==1 || NR>1 { print $1, $2, $9 }'
    else
        echo "Puerto $PORT est├А CERRADO o no se puede determinar el servicio."
    fi
}


